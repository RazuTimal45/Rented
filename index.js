function mouseenter1(){
    document.getElementById('main-text').style.fontSize = "60px";
}
function mouseleave1(){
    document.getElementById('main-text').style.fontSize = "";
}

function toggle1(){
    var list = document.getElementById('detail_info1');
    
    if(list.style.display === "none"){
        list.style.display="block";
        document.getElementById('detail_btn1').innerHTML="Show Less Detail";
    }
    else if(list.style.display === "block"){
        list.style.display="none";
        document.getElementById('detail_btn1').innerHTML="Show More Detail";
    }
    else{
        list.style.display="none";
        document.getElementById('detail_btn1').innerHTML="Show More Detail";
    }
}
